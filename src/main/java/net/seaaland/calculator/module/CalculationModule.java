package net.seaaland.calculator.module;

public class CalculationModule {

    public int getAddition(int o, int t) {
        return o + t;
    }

    public int getSubtraction(int o, int t) {
        return o - t;
    }

    public int getMultiplication(int o, int t) {
        return o * t;
    }

    public int getDivision(int o, int t) {
        return o / t;
    }
}
